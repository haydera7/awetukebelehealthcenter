export const useTranslation = (preferredLanguage = 'English') => {
  const lang = ['English', 'Amharic', 'Oromic'].includes(preferredLanguage) ? preferredLanguage : 'English';

  const t = (key, replacements = {}) => {
    let text = translations[lang]?.[key] || translations['English']?.[key] || key;
    Object.keys(replacements).forEach(replaceKey => {
      text = text.replace(`{${replaceKey}}`, replacements[replaceKey]);
    });
    return text;
  };

  return { t, currentLang: lang };
};
